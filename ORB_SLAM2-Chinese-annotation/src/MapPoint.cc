float MapPoint::GetFoundRatio()
{
    //已被观测到的次数/应该被观测到的次数
    unique_lock<mutex> lock(mMutexFeatures);
    return static_cast<float>(mnFound)/mnVisible;
}

void MapPoint::ComputeDistinctiveDescriptors()
{
    // Retrieve all observed descriptors
    vector<cv::Mat> vDescriptors;

    map<KeyFrame*,size_t> observations;

    {
        //上锁
        unique_lock<mutex> lock1(mMutexFeatures);
        //如果地标点是坏的
        if(mbBad)
            return;
        //observation是能观察到该地标点的关键帧集合
        observations=mObservations;
    }

    if(observations.empty())
        return;
    //vDescriptors矩阵的维数为observations的帧数
    vDescriptors.reserve(observations.size());

    for(map<KeyFrame*,size_t>::iterator mit=observations.begin(), mend=observations.end(); mit!=mend; mit++)
    {
        //可观测到该地标点的关键帧
        KeyFrame* pKF = mit->first;
        //该关键帧不是坏的
        if(!pKF->isBad())
            //提取该关键帧的描述子矩阵中第s行,  s为该地标点在该关键帧中特征点的序号
            vDescriptors.push_back(pKF->mDescriptors.row(mit->second));
    }

    if(vDescriptors.empty())
        return;

    // Compute distances between them
    const size_t N = vDescriptors.size();

    float Distances[N][N];
    for(size_t i=0;i<N;i++)
    {
        Distances[i][i]=0;
        for(size_t j=i+1;j<N;j++)
        {
            //第i个关键帧与第j个关键帧中关于该地标点的描述子之间的距离
            int distij = ORBmatcher::DescriptorDistance(vDescriptors[i],vDescriptors[j]);
            Distances[i][j]=distij;
            Distances[j][i]=distij;
        }
    }

    // Take the descriptor with least median distance to the rest
    int BestMedian = INT_MAX;
    int BestIdx = 0;
    for(size_t i=0;i<N;i++)
    {
        //第i个关键帧的描述子与第1~N个关键帧描述子的距离
        //相当于提取了Distances的第i行
        vector<int> vDists(Distances[i],Distances[i]+N);
        sort(vDists.begin(),vDists.end());
        //选取中值
        int median = vDists[0.5*(N-1)];
        //得到最佳的BestMedian
        if(median<BestMedian)
        {
            BestMedian = median;
            //最佳的观测帧
            BestIdx = i;
        }
    }

    {
        unique_lock<mutex> lock(mMutexFeatures);
        //得到该地标点对应最佳的关键帧,描述子
        mDescriptor = vDescriptors[BestIdx].clone();
    }
}

cv::Mat MapPoint::GetDescriptor()
{
    unique_lock<mutex> lock(mMutexFeatures);
    return mDescriptor.clone();
}

int MapPoint::GetIndexInKeyFrame(KeyFrame *pKF)
{
    unique_lock<mutex> lock(mMutexFeatures);
    if(mObservations.count(pKF))
        return mObservations[pKF];
    else
        return -1;
}

bool MapPoint::IsInKeyFrame(KeyFrame *pKF)
{
    unique_lock<mutex> lock(mMutexFeatures);
    return (mObservations.count(pKF));
}
//更新深度与平均角度
void MapPoint::UpdateNormalAndDepth()
{
    map<KeyFrame*,size_t> observations;
    KeyFrame* pRefKF;
    cv::Mat Pos;
    {
        unique_lock<mutex> lock1(mMutexFeatures);
        unique_lock<mutex> lock2(mMutexPos);
        //如果该地标点是坏的
        if(mbBad)
            return;
        //可观测到这个地标点的关键帧
        observations=mObservations;
        //当前关键帧
        pRefKF=mpRefKF;
        //地标点的位姿
        Pos = mWorldPos.clone();
    }

    if(observations.empty())
        return;

    cv::Mat normal = cv::Mat::zeros(3,1,CV_32F);
    int n=0;
    for(map<KeyFrame*,size_t>::iterator mit=observations.begin(), mend=observations.end(); mit!=mend; mit++)
    {
        //可观测的关键帧
        KeyFrame* pKF = mit->first;
        //得到相机的光心
        cv::Mat Owi = pKF->GetCameraCenter();
        //地标点与光心的连线
        cv::Mat normali = mWorldPos - Owi;
        normal = normal + normali/cv::norm(normali);
        n++;
    }
    //当前帧的光心与地标点的连线
    cv::Mat PC = Pos - pRefKF->GetCameraCenter();
    //计算距离
    const float dist = cv::norm(PC);
    //observations[pRefKF]]表示在pRefKF帧中该地标点的特征序号
    //octave表示金字塔的第几层
    const int level = pRefKF->mvKeysUn[observations[pRefKF]].octave;
    const float levelScaleFactor =  pRefKF->mvScaleFactors[level];
    const int nLevels = pRefKF->mnScaleLevels;

    {
        unique_lock<mutex> lock3(mMutexPos);
        //现在知道了
        mfMaxDistance = dist*levelScaleFactor;//levelScaleFactor为该地标点在参考帧下的尺度, 通过相乘,可以得到金字塔第0层到地标点的距离
        mfMinDistance = mfMaxDistance/pRefKF->mvScaleFactors[nLevels-1];//得到金字塔第7层到地标点的距离
        //地标点与光心连线的单位向量的平均值，即平均观测方向
        mNormalVector = normal/n;
    }
}

float MapPoint::GetMinDistanceInvariance()
{
    unique_lock<mutex> lock(mMutexPos);
    return 0.8f*mfMinDistance;
}

float MapPoint::GetMaxDistanceInvariance()
{
    unique_lock<mutex> lock(mMutexPos);
    return 1.2f*mfMaxDistance;
}

//利用光心与地标点的距离预测特征点在金字塔的第几层数
int MapPoint::PredictScale(const float &currentDist, KeyFrame* pKF)
{
    float ratio;
    {
        unique_lock<mutex> lock(mMutexPos);
        //最大距离和当前距离的比值
        ratio = mfMaxDistance/currentDist;
    }
    //这里看不懂
    int nScale = ceil(log(ratio)/pKF->mfLogScaleFactor);//取对数, 1.2的幂次
    if(nScale<0)
        nScale = 0;
    else if(nScale>=pKF->mnScaleLevels)//大于8
        nScale = pKF->mnScaleLevels-1;//则认为是7

    return nScale;
}

int MapPoint::PredictScale(const float &currentDist, Frame* pF)
{
    float ratio;
    {
        unique_lock<mutex> lock(mMutexPos);
        ratio = mfMaxDistance/currentDist;
    }
    //这里看不懂
    int nScale = ceil(log(ratio)/pF->mfLogScaleFactor);
    if(nScale<0)
        nScale = 0;
    else if(nScale>=pF->mnScaleLevels)
        nScale = pF->mnScaleLevels-1;

    return nScale;
}



